/* ---------- i18n Engine ---------- */
let currentLang = localStorage.getItem('ds_lang') || 'en';

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('ds_lang', lang);

  // Update html lang attribute
  document.documentElement.lang = lang === 'hi' ? 'hi' : lang === 'kn' ? 'kn' : 'en';

  // Update all translatable elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = TRANSLATIONS[lang][key];
    if (translation) {
      el.innerHTML = translation;
    }
  });

  // Update active button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Update font families for Hindi/Kannada
  if (lang === 'hi') {
    document.body.style.setProperty('--font-body-override', "'Noto Sans Devanagari', 'Lora', serif");
  } else if (lang === 'kn') {
    document.body.style.setProperty('--font-body-override', "'Noto Sans Kannada', 'Lora', serif");
  } else {
    document.body.style.removeProperty('--font-body-override');
  }
}

// Initialize language on load
document.addEventListener('DOMContentLoaded', () => {
  // Set up language switcher
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      setLanguage(btn.dataset.lang);
    });
  });

  // Apply saved language
  if (currentLang !== 'en') {
    setLanguage(currentLang);
  }
});
